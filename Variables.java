 public class Variables {

    public static void main(String[] args) {

        int age = 20;
        double marks = 85.5;
        char grade = 'A';
        boolean pass = true;
        String name = "Ekta";

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Marks: " + marks);
        System.out.println("Grade: " + grade);
        System.out.println("Pass: " + pass);
    }
}